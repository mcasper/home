defmodule Budget.Plaid do
  @moduledoc """
  The Plaid context.
  """

  import Ecto.Query, warn: false
  alias Budget.Repo

  alias Budget.Plaid.Item

  @doc """
  Returns the list of items.

  ## Examples

      iex> list_items()
      [%Item{}, ...]

  """
  def list_items do
    Repo.all(Item)
  end

  @doc """
  Gets a single item.

  Raises `Ecto.NoResultsError` if the Item does not exist.

  ## Examples

      iex> get_item!(123)
      %Item{}

      iex> get_item!(456)
      ** (Ecto.NoResultsError)

  """
  def get_item!(id), do: Repo.get!(Item, id)

  @doc """
  Creates a item.

  ## Examples

      iex> create_item(%{field: value})
      {:ok, %Item{}}

      iex> create_item(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_item(attrs \\ %{}) do
    %Item{}
    |> Item.changeset(attrs)
    |> Repo.insert()
  end

  @doc """
  Updates a item.

  ## Examples

      iex> update_item(item, %{field: new_value})
      {:ok, %Item{}}

      iex> update_item(item, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_item(%Item{} = item, attrs) do
    item
    |> Item.changeset(attrs)
    |> Repo.update()
  end

  @doc """
  Deletes a Item.

  ## Examples

      iex> delete_item(item)
      {:ok, %Item{}}

      iex> delete_item(item)
      {:error, %Ecto.Changeset{}}

  """
  def delete_item(%Item{} = item) do
    Repo.delete(item)
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking item changes.

  ## Examples

      iex> change_item(item)
      %Ecto.Changeset{source: %Item{}}

  """
  def change_item(%Item{} = item) do
    Item.changeset(item, %{})
  end

  def delete_existing_items_for_user(%Budget.Accounts.User{} = user) do
    Repo.delete_all(from(i in Item, where: i.user_id == ^user.id))
  end

  alias Budget.Plaid.IgnoredTransaction

  def create_ignored_transaction(attrs \\ %{}) do
    %IgnoredTransaction{}
    |> IgnoredTransaction.changeset(attrs)
    |> Repo.insert()
  end

  alias Budget.Plaid.CategorizedTransaction

  def create_categorized_transaction(attrs \\ %{}) do
    %CategorizedTransaction{}
    |> CategorizedTransaction.changeset(attrs)
    |> Repo.insert()
  end
end
